local v = ...

if v < 0 then
    return '255,0,255,0'
elseif v < 0.5 then
    -- 255,  0,255 @ 0.167
    --   0,  0,255 @ 0.333
    --   0,255,255 @ 0.5
    return ((v - 0.333) * -1530)..','..((v - 0.333) * 1530)..',255,'..(v * 1530)
else
    --   0,255,  0 @ 0.667
    -- 255,255,  0 @ 0.833
    -- 255,  0,  0 @ 1
    return ((v - 0.667) * 1530)..','..((v - 1) * -1530)..','..((v - 0.667) * -1530)
end
