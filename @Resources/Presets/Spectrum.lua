local v = ...

if v < 0 then
  return '255,0,0,0'
elseif v < 0.5 then
  -- 255,  0,  0 @ 0.167
  -- 255,255,  0 @ 0.333
  --   0,255,  0 @ 0.5
  return ((v - 0.5) * -1530)..','..((v - 0.167) * 1530)..',0,'..(v * 1530)
else
  --   0,255,255 @ 0.667
  --   0,  0,255 @ 0.833
  -- 255,  0,255 @ 1
  return ((v - 0.833) * 1530)..','..((v - 0.833) * -1530)..','..((v - 0.5) * 1530)
end
